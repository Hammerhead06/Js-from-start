const month="jan";
switch (month) {
    case "jan":
        console.log("january");
        break;
    case "feb":
        console.log("february");
        break;
    case "mar":
        console.log("march");
        break;
    case "apr":
        console.log("april");
        break;
    case "may":
        console.log("may");
        break;

    default:
        console.log("invalid")
        break;
}